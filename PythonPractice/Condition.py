#if
x = 10

if x > 5:
    print("x is greater than 5")

#if else
x = 3

if x % 2 == 0:
    print("x is even")
else:
    print("x is odd")

#if elif else
x = 10

if x > 10:
    print("x is greater than 10")
elif x == 10:
    print("x is equal to 10")
else:
    print("x is less than 10")

